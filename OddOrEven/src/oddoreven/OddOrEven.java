
package oddoreven;


public class OddOrEven {

    
    public static void main(String[] args) {
        
        int num ;
        num = 3;
        
        if (num % 2 == 0){
            System.out.println(num + " is even");
        }
        else if (num % 2 != 0) {
            System.out.println(num + " is odd");
            
        }
                
     
    }
    
}
